export type SystemCopy = DeepString<typeof ru>;

export function fillSystemCopy(template: string, values: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

export function useSystemCopy() {
  const { language, locale } = useLanguage();
  const copy: SystemCopy = language === 'ru' ? ru : en;
  return { copy, language, locale };
}
