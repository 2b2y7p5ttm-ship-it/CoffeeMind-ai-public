export type SectionCopy = DeepString<typeof ru>;
export type BooksCopy = SectionCopy['books'];
export type StatsCopy = SectionCopy['stats'];
export type ProfileCopy = SectionCopy['profile'];

export function fillSectionCopy(template: string, values: Record<string, string | number>): string {
  return template.replace(/\{(\w+)\}/g, (_, key: string) => String(values[key] ?? `{${key}}`));
}

export function useSectionCopy() {
  const { language, locale } = useLanguage();
  const copy: SectionCopy = language === 'ru' ? ru : en;
  return { copy, language, locale };
}
